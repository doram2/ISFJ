public class Ex12 {
	int first;
	int second;
	String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getFirst() {
		return first;
	}
	public int getSecond() {
		return second;
	}
	public void setFirst(int first) {
		this.first = first;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	
	public int add() {
		return this.first + this.second;
	}
	public int sub() {
		return this.first - this.second;
	}
	
	
}
